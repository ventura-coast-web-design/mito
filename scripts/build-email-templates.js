const fs = require("fs");
const path = require("path");

const sourceDir = path.join(__dirname, "../netlify/functions/lib/emails");
const outputFile = path.join(__dirname, "../netlify/functions/lib/email-templates.js");

const templates = {
  html: fs.readFileSync(path.join(sourceDir, "registration-confirmed.html"), "utf8"),
  text: fs.readFileSync(path.join(sourceDir, "registration-confirmed.txt"), "utf8"),
};

fs.writeFileSync(
  outputFile,
  `// Generated by scripts/build-email-templates.js — do not edit directly.\nmodule.exports = ${JSON.stringify(templates, null, 2)};\n`,
  "utf8"
);

console.log("Wrote", path.relative(process.cwd(), outputFile));
